#ifndef EXHAUST_FP_H
#define EXHAUST_FP_H

/*
 * Sequentially read lines from `fp' until EOF or error arrives and
 * call `process' for each line.
 * If `process' returns -1, then abort the whole program.
 * The line, passed to `process' via the char* parameter will be overwritten
 * by next invocations.
 */
void exhaust_fp(FILE *fp, void (*process)(char *, void *), void *process_arg);

#endif  /* EXHAUST_FP_H */

